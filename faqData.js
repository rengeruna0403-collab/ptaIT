function searchFAQ(input) {
  const normalized = input.toLowerCase().replace(/[！-～]/g, s =>
    String.fromCharCode(s.charCodeAt(0) - 0xFEE0)
  );

  const scored = faqList.map(faq => {
    let score = 0;
    for (const kw of faq.keywords) {
      if (normalized.includes(kw.toLowerCase())) {
        score += kw.length;
      }
    }
    return { faq, score };
  }).filter(x => x.score > 0)
    .sort((a, b) => b.score - a.score);

  return scored.map(x => x.faq);
}

function getFAQByCategory(categoryId) {
  return faqList.filter(faq => faq.category === categoryId && !faq.ambiguous);
}

function getFAQById(id) {
  return faqList.find(faq => faq.id === id);
}

// ── カテゴリのトップFAQ（ambiguousエントリ）を取得する
function getCategoryTopFAQ(categoryId) {
  return faqList.find(faq => faq.category === categoryId && faq.ambiguous &&
    (faq.id === categoryId + "_top" || faq.id.endsWith("_top")));
}
